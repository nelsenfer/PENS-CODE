#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    double x = 9.0;

    cout << "akar kuadrat dari 9: " << " adalah " << sqrt(x) << endl;
    cout << "2 pangkat 5 adalah " << pow(2, 5) << endl;
    cout << "nilai absolut dari -12 adalah " << abs(-12) << endl;
    cout << "pembulatan 3.14 adalah " << round(3.14) << endl;
    cout << "pembulatan keatas 3.14 adalah " << ceil(3.14) << endl;
    cout << "pembulatan kebawah 3.14 adalah " << floor(3.14) << endl;
    cout << "sinus 90 derajat (pi/2 rad) " << sin(M_PI / 2) << endl;

    return 0;
}